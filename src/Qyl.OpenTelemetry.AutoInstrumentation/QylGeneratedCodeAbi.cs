namespace Qyl.OpenTelemetry.AutoInstrumentation.GeneratedCode;

/// <summary>
/// Version anchor for the generated-code ABI. Generated interceptors reference the
/// current anchor constant; the constant is renamed on every breaking ABI change so
/// generated code from a mismatched generator version fails to compile instead of
/// silently binding to changed runtime behavior.
/// </summary>
[System.ComponentModel.EditorBrowsable(System.ComponentModel.EditorBrowsableState.Never)]
public static class QylGeneratedCodeAbi
{
    /// <summary>The 6.x generated-code ABI anchor.</summary>
    public const int V6 = 6;
}
