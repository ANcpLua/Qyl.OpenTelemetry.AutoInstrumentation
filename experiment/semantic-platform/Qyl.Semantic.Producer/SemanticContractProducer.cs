using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace Qyl.Semantic.Producer;

/// <summary>
/// PRODUCER (1 of the 1→N platform). Reads a domain semantic-contract additional file and emits the
/// shared contract into the INITIAL compilation via RegisterPreCompilationSourceOutput (#83088):
///   - the shared attribute type <c>Qyl.Semantic.QylSemanticBindingAttribute</c>, and
///   - for each contract row, an attribute-decorated <c>partial</c> of the user's type.
/// Because this is pre-compilation output, it is visible to EVERY other generator's standard phase —
/// so consumers (OTel, Logging, …) bind to it WITHOUT referencing this producer.
///
/// Why an additional file and not DTO inference: the pre-compilation phase may not touch the
/// compilation/syntax (it throws). DTO-name inference therefore runs as a standard-phase analyzer
/// that PRODUCES this contract file; publishing a DTO-derived contract cross-generator in one pass
/// would need RegisterDeclarationOutput (#81395), which is unmerged. See the platform verdict.
/// </summary>
[Generator(LanguageNames.CSharp)]
public sealed class SemanticContractProducer : IIncrementalGenerator
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var contract = context.AdditionalTextsProvider
            .Where(static t => t.Path.Replace('\\', '/').EndsWith("semantic-contract.tsv", StringComparison.OrdinalIgnoreCase))
            .Select(static (t, ct) => t.GetText(ct)?.ToString() ?? string.Empty)
            .Collect();

        context.RegisterPreCompilationSourceOutput(contract, static (spc, texts) =>
        {
            spc.AddSource("QylSemanticBindingAttribute.g.cs", SourceText.From(AttributeSource, Encoding.UTF8));

            var rows = Parse(string.Concat(texts));
            foreach (var typeGroup in rows.GroupBy(r => r.TypeFqn, StringComparer.Ordinal))
                spc.AddSource(HintFor(typeGroup.Key), SourceText.From(BuildPartial(typeGroup.Key, typeGroup.ToList()), Encoding.UTF8));
        });
    }

    private const string AttributeSource =
"""
// <auto-generated/> shared semantic-contract vocabulary, emitted into the initial compilation.
#nullable enable
namespace Qyl.Semantic;

/// <summary>One inferred semantic-convention binding: a property of the annotated type maps to an
/// OpenTelemetry attribute key. Read by any consumer generator via the semantic model.</summary>
[global::System.AttributeUsage(global::System.AttributeTargets.Class, AllowMultiple = true, Inherited = false)]
public sealed class QylSemanticBindingAttribute : global::System.Attribute
{
    public QylSemanticBindingAttribute(string property, string attribute)
    {
        Property = property;
        Attribute = attribute;
    }

    public string Property { get; }
    public string Attribute { get; }
}
""";

    private readonly record struct Row(string TypeFqn, string Property, string Attribute);

    private static List<Row> Parse(string tsv)
    {
        var rows = new List<Row>();
        foreach (var raw in tsv.Split('\n'))
        {
            var line = raw.TrimEnd('\r');
            if (line.Length == 0 || line[0] == '#') continue;
            var c = line.Split('\t');
            if (c.Length < 3) continue;
            rows.Add(new Row(c[0].Trim(), c[1].Trim(), c[2].Trim()));
        }
        return rows;
    }

    private static string HintFor(string typeFqn) => typeFqn.Replace('.', '_') + ".Contract.g.cs";

    private static string BuildPartial(string typeFqn, List<Row> rows)
    {
        // DEMO SCOPE: the contract FQN column addresses TOP-LEVEL types (Namespace.Type). Nested
        // types (metadata 'Outer+Inner') are out of scope — a dotted split cannot distinguish a
        // namespace segment from an outer-type segment, so they would silently mis-target. A
        // production version would carry the containing-type chain explicitly.
        var lastDot = typeFqn.LastIndexOf('.');
        var ns = lastDot < 0 ? null : typeFqn.Substring(0, lastDot);
        var name = lastDot < 0 ? typeFqn : typeFqn.Substring(lastDot + 1);

        var b = new StringBuilder();
        b.AppendLine("// <auto-generated/> compiler-visible semantic contract (pre-compilation output).");
        b.AppendLine("#nullable enable");
        if (ns is not null) b.AppendLine($"namespace {ns};").AppendLine();
        foreach (var r in rows)
            b.AppendLine($"[global::Qyl.Semantic.QylSemanticBindingAttribute(\"{Escape(r.Property)}\", \"{Escape(r.Attribute)}\")]");
        b.AppendLine($"public partial class {name} {{ }}");
        return b.ToString();
    }

    private static string Escape(string s) => s.Replace("\\", "\\\\").Replace("\"", "\\\"");
}
