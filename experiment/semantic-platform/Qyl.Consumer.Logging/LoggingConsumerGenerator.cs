using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace Qyl.Consumer.Logging;

/// <summary>
/// CONSUMER #2 of the 1→N platform. Independent of the producer and of consumer #1. Binds the same
/// shared contract (<c>Qyl.Semantic.QylSemanticBindingAttribute</c>) and projects it the structured-
/// logging way: scope fields keyed by the PROPERTY name (not the semconv attribute). Same contract,
/// divergent consumption — the point of the semantic-platform architecture.
/// </summary>
[Generator(LanguageNames.CSharp)]
public sealed class LoggingConsumerGenerator : IIncrementalGenerator
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        context.RegisterSourceOutput(context.CompilationProvider, static (spc, compilation) =>
        {
            var marker = compilation.GetTypeByMetadataName("Qyl.Semantic.QylSemanticBindingAttribute");
            if (marker is null) return;

            var bound = Collect(compilation, marker);
            if (bound.Count == 0) return;

            var b = new StringBuilder();
            b.AppendLine("// <auto-generated/> Logging consumer — bound to the shared semantic contract.");
            b.AppendLine("#nullable enable");
            b.AppendLine("namespace Qyl.Generated.Logging;");
            b.AppendLine("public static class LogScopeFields");
            b.AppendLine("{");
            foreach (var t in bound)
            {
                b.AppendLine($"    /// <summary>Structured-logging scope fields for <see cref=\"global::{t.Fqn}\"/> (keyed by property).</summary>");
                b.AppendLine($"    public static global::System.Collections.Generic.KeyValuePair<string, object?>[] For(global::{t.Fqn} value) => new global::System.Collections.Generic.KeyValuePair<string, object?>[]");
                b.AppendLine("    {");
                foreach (var (prop, _) in t.Bindings)
                    b.AppendLine($"        new(\"{prop}\", value.{prop}),");
                b.AppendLine("    };");
            }
            b.AppendLine("}");
            spc.AddSource("LogScopeFields.g.cs", SourceText.From(b.ToString(), Encoding.UTF8));
        });
    }

    private readonly record struct Bound(string Fqn, List<(string Property, string Attribute)> Bindings);

    private static List<Bound> Collect(Compilation compilation, INamedTypeSymbol marker)
    {
        var result = new List<Bound>();
        foreach (var type in EnumerateSourceTypes(compilation.GlobalNamespace))
        {
            var bindings = new List<(string, string)>();
            foreach (var a in type.GetAttributes())
            {
                if (SymbolEqualityComparer.Default.Equals(a.AttributeClass, marker) &&
                    a.ConstructorArguments.Length == 2 &&
                    a.ConstructorArguments[0].Value is string prop &&
                    a.ConstructorArguments[1].Value is string attr)
                {
                    bindings.Add((prop, attr));
                }
            }
            if (bindings.Count > 0)
                result.Add(new Bound(type.ToDisplayString(), bindings.OrderBy(x => x.Item1, StringComparer.Ordinal).ToList()));
        }
        return result.OrderBy(x => x.Fqn, StringComparer.Ordinal).ToList();
    }

    private static IEnumerable<INamedTypeSymbol> EnumerateSourceTypes(INamespaceSymbol ns)
    {
        foreach (var t in ns.GetTypeMembers())
            if (t.Locations.Any(l => l.IsInSource))
                yield return t;
        foreach (var child in ns.GetNamespaceMembers())
            foreach (var t in EnumerateSourceTypes(child))
                yield return t;
    }
}
