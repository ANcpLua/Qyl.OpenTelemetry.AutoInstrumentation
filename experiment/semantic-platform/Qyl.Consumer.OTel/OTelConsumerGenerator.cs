using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace Qyl.Consumer.OTel;

/// <summary>
/// CONSUMER #1 of the 1→N platform. Does NOT reference the producer. In its standard phase it binds
/// the producer's pre-compilation contract (<c>Qyl.Semantic.QylSemanticBindingAttribute</c>, visible
/// because it was emitted into the initial compilation) and projects it the OTel way: tags keyed by
/// the semantic-convention attribute (<c>customer.id</c>).
/// </summary>
[Generator(LanguageNames.CSharp)]
public sealed class OTelConsumerGenerator : IIncrementalGenerator
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        context.RegisterSourceOutput(context.CompilationProvider, static (spc, compilation) =>
        {
            var marker = compilation.GetTypeByMetadataName("Qyl.Semantic.QylSemanticBindingAttribute");
            if (marker is null) return;

            var bound = ContractReader.Collect(compilation, marker);
            if (bound.Count == 0) return;

            var b = new StringBuilder();
            b.AppendLine("// <auto-generated/> OTel consumer — bound to the shared semantic contract.");
            b.AppendLine("#nullable enable");
            b.AppendLine("namespace Qyl.Generated.OTel;");
            b.AppendLine("public static class OTelSemanticTags");
            b.AppendLine("{");
            foreach (var t in bound)
            {
                b.AppendLine($"    /// <summary>OTel attribute tags for <see cref=\"global::{t.TypeFqn}\"/> (keyed by semconv attribute).</summary>");
                b.AppendLine($"    public static global::System.Collections.Generic.KeyValuePair<string, object?>[] For(global::{t.TypeFqn} value) => new global::System.Collections.Generic.KeyValuePair<string, object?>[]");
                b.AppendLine("    {");
                foreach (var (prop, attr) in t.Bindings)
                    b.AppendLine($"        new(\"{attr}\", value.{prop}),");
                b.AppendLine("    };");
            }
            b.AppendLine("}");
            spc.AddSource("OTelSemanticTags.g.cs", SourceText.From(b.ToString(), Encoding.UTF8));
        });
    }
}

internal static class ContractReader
{
    public readonly record struct BoundType(string TypeFqn, IReadOnlyList<(string Property, string Attribute)> Bindings);

    public static List<BoundType> Collect(Compilation compilation, INamedTypeSymbol marker)
    {
        var result = new List<BoundType>();
        foreach (var type in EnumerateSourceTypes(compilation.GlobalNamespace))
        {
            var bindings = new List<(string, string)>();
            foreach (var a in type.GetAttributes())
            {
                if (!SymbolEqualityComparer.Default.Equals(a.AttributeClass, marker)) continue;
                if (a.ConstructorArguments.Length == 2 &&
                    a.ConstructorArguments[0].Value is string prop &&
                    a.ConstructorArguments[1].Value is string attr)
                {
                    bindings.Add((prop, attr));
                }
            }
            if (bindings.Count > 0)
                result.Add(new BoundType(type.ToDisplayString(), bindings.OrderBy(x => x.Item1, StringComparer.Ordinal).ToList()));
        }
        return result.OrderBy(x => x.TypeFqn, StringComparer.Ordinal).ToList();
    }

    private static IEnumerable<INamedTypeSymbol> EnumerateSourceTypes(INamespaceSymbol ns)
    {
        foreach (var t in ns.GetTypeMembers())
            if (t.Locations.Any(l => l.IsInSource))
                yield return t;
        foreach (var child in ns.GetNamespaceMembers())
            foreach (var t in EnumerateSourceTypes(child))
                yield return t;
    }
}
